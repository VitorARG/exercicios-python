a1 = input('digite algo')
print('O tipo primitivo desse valor é {}'.format(type(a1)))
print('So tem espaços? {}!'.format(a1.isspace()))
print('É um numero? {}!'.format(a1.isnumeric()))
print('É alfabetico? {}!'.format(a1.isalpha()))
print('É alfanumérico? {}!'.format(a1.isalnum()))
print('Esta em letras maiusculas? {}!'.format(a1.isupper()))
print('Esta em letras minusculas? {}!'.format(a1.islower()))
print('esta capitalizada? {}!'.format(a1.istitle()))





